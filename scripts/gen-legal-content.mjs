// Generates the typed legal content files (src/content/legal/{privacy,terms,accessibility}.ts) from the
// reviewed source in scripts/legal-content-data.json. The source is the output of the legal-content
// drafting/verification workflow; this script applies a few documented corrections and formats it as TS.
//
// First run (bootstraps the snapshot from the raw workflow output):
//   node scripts/gen-legal-content.mjs <path-to-workflow-output.json>
// Subsequent runs (use the committed snapshot):
//   node scripts/gen-legal-content.mjs
import { readFileSync, writeFileSync } from "node:fs";

const SNAPSHOT = "scripts/legal-content-data.json";
const OUT_DIR = "src/content/legal";

function loadDocuments() {
  const sourcePath = process.argv[2];
  if (!sourcePath) return JSON.parse(readFileSync(SNAPSHOT, "utf8"));

  const raw = JSON.parse(readFileSync(sourcePath, "utf8"));
  const result = raw.result ?? raw;
  const documents = { privacy: result.privacy, terms: result.terms, accessibility: result.accessibility };
  writeFileSync(SNAPSHOT, `${JSON.stringify(documents, null, 2)}\n`);
  return documents;
}

function sectionById(doc, id) {
  return doc.sections.find((section) => section.id === id);
}

function runsStartWith(block, prefix) {
  return block.kind === "note" && block.runs.some((run) => run.text?.startsWith(prefix));
}

// The reviewer pass repurposed the "retentionPeriod" fact in three spots where no retention value belongs.
// The "Last updated" date is already shown in the page header, so we remove the redundant in-body dates and
// drop the misused fact placeholders rather than ship a wrong token.
function applyCorrections(documents) {
  const terms = sectionById(documents.terms, "acceptance");
  terms.blocks = terms.blocks.filter((block) => !runsStartWith(block, "These Terms were last updated"));

  const commitment = sectionById(documents.accessibility, "commitment");
  for (const block of commitment.blocks) {
    if (runsStartWith(block, "This statement last updated")) {
      block.runs = [
        {
          text: "This statement applies to this website. Our conformance claims are based on our own internal review and ongoing testing rather than a formal third-party audit.",
        },
      ];
    }
  }

  const feedback = sectionById(documents.accessibility, "feedback-contact");
  for (const block of feedback.blocks) {
    if (runsStartWith(block, "We aim to acknowledge accessibility feedback")) {
      block.runs = [
        {
          text: "We aim to acknowledge accessibility feedback promptly and to provide requested information in an alternative format within a reasonable timeframe. There is no charge for requesting an alternative format.",
        },
      ];
    }
  }

  const rights = sectionById(documents.privacy, "your-rights");
  for (const block of rights.blocks) {
    for (const run of block.runs ?? []) {
      if (run.text?.includes("Commission d'acces a l'information")) {
        run.text = run.text.replace("Commission d'acces a l'information", "Commission d'accès à l'information");
      }
    }
  }

  return documents;
}

// The workflow used the compact block tag "p"; the project type uses the readable "paragraph".
function normalizeBlock(block) {
  return block.kind === "p" ? { ...block, kind: "paragraph" } : block;
}

function normalizeDocument(doc) {
  return {
    title: doc.title,
    summary: doc.summary,
    lastUpdated: "__LAST_UPDATED__",
    sections: doc.sections.map((section) => ({
      id: section.id,
      heading: section.heading,
      blocks: section.blocks.map(normalizeBlock),
    })),
  };
}

function toTypeScript(exportName, doc) {
  const literal = JSON.stringify(normalizeDocument(doc), null, 2)
    .replace(/"([a-zA-Z_$][a-zA-Z0-9_$]*)":/g, "$1:")
    .replace('lastUpdated: "__LAST_UPDATED__"', "lastUpdated: legalLastUpdated");

  return [
    `// Generated by scripts/gen-legal-content.mjs from scripts/legal-content-data.json — edit there, not here.`,
    `import type { LegalDocument } from "@/types";`,
    `import { legalLastUpdated } from "./legal-facts";`,
    ``,
    `export const ${exportName}: LegalDocument = ${literal};`,
    ``,
  ].join("\n");
}

function countRuns(doc, predicate) {
  let total = 0;
  for (const section of doc.sections) {
    for (const block of section.blocks) {
      const runGroups = [block.runs, ...(block.items ?? []), ...(block.entries ?? []).map((e) => e.runs)];
      for (const runs of runGroups) {
        for (const run of runs ?? []) if (predicate(run)) total += 1;
      }
    }
  }
  return total;
}

const documents = applyCorrections(loadDocuments());
const files = {
  privacy: "privacyPolicy",
  terms: "termsAndConditions",
  accessibility: "accessibilityStatement",
};

for (const [slug, exportName] of Object.entries(files)) {
  const doc = documents[slug];
  writeFileSync(`${OUT_DIR}/${slug}.ts`, toTypeScript(exportName, doc));
  const facts = countRuns(doc, (run) => "fact" in run);
  const links = countRuns(doc, (run) => Boolean(run.href));
  console.log(`${slug}.ts — ${doc.sections.length} sections, ${facts} fact tokens, ${links} links`);
}
