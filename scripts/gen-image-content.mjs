// Reads public/images/image-manifest.json → emits src/content/site-images.ts (typed) + public/images/CREDITS.md.
import { readFileSync, writeFileSync } from "node:fs";
import { join } from "node:path";

const ROOT = process.cwd();
const manifest = JSON.parse(readFileSync(join(ROOT, "public", "images", "image-manifest.json"), "utf8"));

// --- site-images.ts ---
const entries = Object.entries(manifest)
  .map(([id, m]) => {
    return `  ${JSON.stringify(id)}: {\n` +
      `    src: ${JSON.stringify(m.src)},\n` +
      `    width: ${m.width},\n` +
      `    height: ${m.height},\n` +
      `    alt: ${JSON.stringify(m.alt)},\n` +
      `    blurDataURL: ${JSON.stringify(m.blurDataURL)},\n` +
      `  },`;
  })
  .join("\n");

const ts = `// AUTO-GENERATED by scripts/gen-image-content.mjs from public/images/image-manifest.json — do not edit by hand.
// Every photo is downloaded locally + optimised (WebP q82, EXIF-stripped) with a base64 blur placeholder.
// Sources + licences are in public/images/CREDITS.md.

export interface SiteImage {
  src: string;
  width: number;
  height: number;
  alt: string;
  blurDataURL: string;
}

export const siteImages = {
${entries}
} satisfies Record<string, SiteImage>;

export type SiteImageId = keyof typeof siteImages;
`;
writeFileSync(join(ROOT, "src", "content", "site-images.ts"), ts);

// --- CREDITS.md ---
const credits = JSON.parse(readFileSync(join(ROOT, "scripts", "credits-data.json"), "utf8"));
const rows = credits
  .map((c) => {
    const source = c.placeholder ? "— (generated placeholder)" : `[${c.sourceName ?? "Wikimedia Commons"}](${c.source})`;
    return `| \`${c.file}\` | ${source} | ${c.author} | ${c.license} |`;
  })
  .join("\n");

const md = `# Image credits & licensing

Every photograph on this site is **downloaded locally** into \`public/images/\` (never hot-linked) and re-encoded to
WebP (quality ~82, EXIF stripped) with a base64 blur placeholder. Photos come from **Wikimedia Commons** and
**Flickr** under free / Creative Commons / public-domain licences. Attribution for CC-BY / CC-BY-SA files is recorded below.

> **Manufacturer assets are NOT scraped.** Partner brand marks (Lorex, Hikvision, HiLook) and exact model product
> shots are trademarked; the partner logos here are neutral text lockups (placeholders) and the camera-type images
> are generic stock stand-ins. See "Client-owned assets still needed" below.

## Photographs (${credits.filter((c) => !c.placeholder).length} files)

| File | Source | Author | License |
|---|---|---|---|
${rows}

## Partner logos (placeholders — \`public/images/partners/\`)

\`lorex.svg\`, \`hikvision.svg\`, \`hilook.svg\` are **neutral text-logo lockups built in-house** — they are NOT the
official brand marks. Do not present them as authorized brand assets.

## Client-owned assets still needed (TODO — replace before launch)

- **Real Soo Solutions install photos** — the \`installs/\` images are generic stock stand-ins; replace with the
  company's own photographed jobs (with the customer's consent).
- **Official partner logos** — replace \`partners/*.svg\` with the official authorized-dealer logo assets from
  Lorex / Hikvision / HiLook press kits.
- **Exact model product shots** — the \`cameras/\` images illustrate the *form factor* with generic stock; replace
  with the authorized product photography for the specific Lorex / Hikvision / HiLook models sold.
- **Real crew / service-area photo** for the About page.
`;
writeFileSync(join(ROOT, "public", "images", "CREDITS.md"), md);

console.log(`Wrote src/content/site-images.ts (${Object.keys(manifest).length} images) + public/images/CREDITS.md`);
